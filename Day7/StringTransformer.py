# Input sentence
sentence = input("Enter a sentence: ")

# All uppercase
upper_case = sentence.upper()

# All lowercase
lower_case = sentence.lower()

# Title case
title_case = sentence.title()

# Replace all spaces with underscores
underscored = sentence.replace(" ", "_")

# Print results
print("\nTransformed Versions:")
print("1. Uppercase:", upper_case)
print("2. Lowercase:", lower_case)
print("3. Title Case:", title_case)
print("4. Spaces replaced with underscores:", underscored)
